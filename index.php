				<h2>Eutrofizacja</h2>
				<p>Eutrofizacja to proces powodowany zanieczyszczeniem wód morskich
					m.in. nawozami, głównie azotowymi i/lub fosforanami, prowadzący do
					zwiększonego wzrostu, produkcji biomasy glonów, zmian w równowadze
					ekosystemu oraz pogorszenia się jakości wody. Azot pochodzi głównie
					z terenów rolnych, ale również z gazów azotowych, hodowli wodnych,
					wody przemysłowej i przylegających oceanów. Natomiast główne źródło
					fosforanów to domowe i przemysłowe ścieki. Eutrofizacja prowadzi do
					wzmożonego wzrostu glonów, które blokują dostęp światła słonecznego
					do niżej położonych partii dna morskiego. W wyniku tego zmniejsza
					się ilość rosnących tam wodorostów i trawy morskiej. Zwiększony
					rozkład martwych glonów skutkuje deficytem tlenu przy dnie morskim.
					Eutrofizacja zagraża bioróżnorodności i równowadze ekosystemu. Ma
					także skutki ekonomiczne – mniej tlenu w wodzie to mniej ryb
					łowionych przez rybaków, a przedostające się do skorupiaków toksyny
					wydzielane przez kwitnące glony mogą być trujące dla człowieka.
					Jakość wód morskich obniża się przez zanieczyszczenie
					rozkładającymi się glonami a także toksynami z kwitnących glonów,
					co ma wpływ na przemysł turystyczny.</p>
