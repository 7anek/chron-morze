				<h2>Łańcuch troficzny</h2>
				<p>Łańcuchy troficzne zwane pokarmowymi to sieć interakcji
					zachodzących pomiędzy konsumentami a ich pożywieniem. Ta cecha
					opisuje m.in. obieg materii i przepływ energii wewnątrz ekosystemu,
					poziomy produktywnosci kluczowych składowych i strukturę
					ekosystemu. Zmiany w obfitości orgnizmów na jednym poziomie
					powodują zmiany na innych poziomach. Rybołóstwo jest czynnikiem
					najbardziej wpływającym na wielkość zasobów ryb, co ma bezpośredni
					wpływ na całą sieć troficzną. Odławianie dużych ryb powoduje
					większą ich śmiertelność. To sprawia, że bez naturalnych wrogów w
					środowisku morskim wzrasta populacja mniejszych ryb, które są
					zawyczaj ofiarami ryb dużych. Z kolei odławianie małych ryb takich
					jak śledzie, powoduje że kurczą się zasoby pożywienia dla morskich
					ssaków. Zanieczyszczenie środowiska morskiego chemikaliami to
					kolejny czynnik który ma wpływ na łańcuchy troficzne. Substancje
					chemiczne gromadzą się w morskich organizmach i wędrują poprzez
					kolejne ogniwa łańcucha pokarmowego, ostatecznie kumulując się w
					dużych rybach i morskich ssakach.</p>
