			<div id="7">
				<h2>Warunki hydrograficzne</h2>
				<p>Warunki hydrograficzne charakteryzują takie fizyczne parametry
					wody morskiej jak: temperatura, zasolenie, głębokość, prądy, fale,
					zawirowania, zmętnienie. Grają one główną rolę w dynamice morskich
					ekosystemów i mogą być zmieniane przez aktywność ludzi, szczególnie
					w obszarach nadbrzeżnych. Mają także wpływ na produkcję i wzrost
					planktonu, ryb oraz rozkład i siedlisko larw wielu gatunków bentosu
					i pelagialu. Są również istotne w wymianach pomiędzy morzem a
					atmosferą i między różnymi warstwami wody (dzięki czemu możliwe
					jest m.in. odpowiednie natlenowanie warstw wody położonych blisko
					dna morskiego). Warunki hydrograficzne zależą głównie od
					przypływów, ogólnego oceanicznego krążenia wody oraz klimatu.
					Działalność człowieka, szczególnie w rejonach przybrzeżnych, także
					może mieć na nie wpływ, poprzez m.in. konstruowanie przybrzeżnej i
					morskiej infrastruktury, morskich platform i instalacji farm
					wiatrowych, tworzenie kanałów, pogłębianie kanałów nawigacyjnych,
					ruch morski, uwalnianie osadu przez sprzęt do rybołówstwa, morskie
					kopalnie i wydobywanie piachu, uwalnianie dużej ilości ciepłej lub
					słonej wody.</p>
