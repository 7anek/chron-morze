			<div id="2">
				<h2>Garunki obce</h2>
				<p>Gatunki obce to gatunki sztucznie wprowadzone do innego niż ich
					naturalne środowisko, w którym mogą żyć i rozmnażać się stanowiąc
					zagrożenie dla gatunków rodzimych, a co za tym idzie zagrażając
					bioróżnorodności. Obce gatunki są wprowadzane podczas wymiany ludzi
					lub dóbr między krajami bądź kontynentami, np. przez żeglugę.
					Przykładami gatunków obcych są nożeniec atlantycki, ostryżyca
					japońska, a także małgiew piaskołaz. W wielu przypadkach gatunki
					obce nie zagrażają regionalnej ekologii i ekonomii. Niekiedy
					gatunek obcy może okazać się gatunkiem inwazyjnym, powodującym
					ogromne i długotrwałe szkody w nowym środowisku. W sytuacji, gdy
					nie mają naturalnych wrogów i wygrywają z rodzimymi gatunkami
					konkurencję o przestrzeń i żywność, gatunki obce mogą z czasem
					zastąpić gatunki rodzime na danym obszarze. Wprowadzają do nowego
					środowiska choroby, na które gatunki rodzime nie są odporne i
					powodują ich wymieranie. Gatunki obce często przybywają do nowych
					środowisk wraz z wodą balastową statków, przyczepione do ich
					kadłubów lub innych podwodnych elementów. Bywa, że są wylewane do
					zbiorników wodnych przez akwarystów chcących się pozbyć hodowanych
					zwierząt morskich. Wczesne wykrycie intruza w środowisku może
					zapobiec jego rozprzestrzenieniu się i inwazji i zagrożeniu jakie
					stanowi dla gatunków rodzimych. Czasem nowe gatunki są wprowadzane
					do obcych środowisk z powodów ekonomicznych, w celach hodowlanych.</p>
