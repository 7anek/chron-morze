				<h2>Bioróżnorodność</h2>
				<p>Różnorodność biologiczna jest utrzymana na naturalnym poziomie.
					Stan i zasięg siedlisk oraz populacja gatunków zgodne są z
					warunkami naturalnymi: fizycznymi, geograficznymi i klimatycznymi.

					Czym jest bioróżnorodność? Termin bioróżnorodność opisuje
					różnorodność form życia na kuli ziemskiej, w różnych wymiarach: w
					obrębie gatunku, między gatunkami oraz różnorodność ekostemów.
					Termin ten odnosi się do wszystkich form życia i ich zachowań,
					środowisk w których żyją organizmy oraz kompleksowego systemu
					związków między nimi, takich jak łańcuchy pokarmowe i konkurowanie
					o źródła pokarmu. Różnorodność form życia sprawia, że cały
					ekosystem jest bardziej elastyczny i odporny na zmiany zachodzące w
					środowisku. Jedne gatunki szybko zastępują inne, a gatunki
					dysponujące większą zmiennością genetyczną są bardziej odporne na
					stres środowiskowy niż te z bardziej ograniczonym zasięgiem
					kombinacji genetycznych. Zakres liczebności organizmów moskich jest
					trudny do wyobrażenia. Sa te zdolne do życia w ekstremalnie niskich
					temperaturach, natomiast inne żyją z zupełnie innym, ponieważ
					niezwykle gorącym środowisku – organizmy żyjące w miejscach
					uwalniania ogrzewania geotermalnego, są w stanie żyć w temperaturze
					do 400C. Jedne żyją w płytkich wodach z dobrym dostępem do światła
					słonecznego, inne zaś w głębokich wodach gdzie światło słoneczne
					nie dociera wcale. Morskie życie rozpoczyna się od bakterii,
					wirusów i organizmów jednokomórkowych lub planktonu. Organizmy te
					mają zdolność do przekształcania energii słonecznej w pożywienie
					dla innych małych zwierząt morskich. Plankton jest pożywieniem dla
					większych zwierząt takich jak ryby czy inne bezkręgowce. Te
					natomiast są pokarmem dla morskich ssaków, dużych ryb i gadów.</p>
