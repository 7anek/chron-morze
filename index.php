				<h2>Komercyjnie eksploatowane gatunki ryb i skorupiaków</h2>
				<p>To wszystkie te gatunki żyjące w morzu, których połów stanowi dla
					człowieka cel ekonomiczny: ryby, rekiny, skorupiaki, mięczaki,
					rozgwiazdy i meduzy. Regularne odławianie ryb i skorupiaków nie
					powinno wyczerpywać dostępnych rezerw. Aby utrzymać zasoby
					środowiska morskiego na stałym, odpowiednim poziomie, nie powinno
					się przekraczać ustalonego wcześniej, rocznego maksymalnego
					zrownoważonego odłowu (MSY), czyli łowienia takiej ilości ryb i
					skorupiaków któych ubytek nie zbędzie zagrażał produktywności w
					kolejnym roku.</p>
