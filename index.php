				<h2>Substancje zanieczyszczające i efekty ich działania</h2>
				<p>"Substancje niebezpieczne oznaczają substancje lub grupy
					substancji, które są toksyczne, trwałe i podatne na bioakumulację
					oraz inne substancje lub grupy substancji, których poziom osiąga
					stan niepokojący” (Dyrektywa wodna, art. 2(29)) Przykładami takich
					substancji w środowisku morskim są pestycydy, farmaceutyki i metale
					ciężkie. Pochodzą one z nadbrzeżnego przemysłu, żeglugi morskiej,
					zanieczyszczonego powietrza i rzek, poszukiwania i eksploatacji
					ropy, gazu, minerałów. Szkodliwe substancje degradują stan wód
					morskich i mogą powodować poważne zakłócenia w funkcjonowaniu
					ekosystemów. Efekty zanieczyszczenia wód mogą być widoczne dopiero
					po pewnym czasie stałego, przewlekłego narażenia mórz na toksyczne
					substancje. Jest coraz więcej dowodów na to, że substancje
					zanieczyszczające wody morskie mogą wpływać na funkcjonowanie
					układu endokrynnego morskich organizmów oraz powodować choroby.</p>
			</div>
			<div id="9">
				<h2>Substancje zanieczyszczające w rybach i owocach morza</h2>
				<p>Szkodliwe substancje skumulowane w organizmach przenoszą się na
					kolejne piętra łańcucha pokarmowego. Biomagnifikacja to proces w
					wyniku którego następuje wzrost stężenia substancji toksycznej w
					organizmie zajmującym wyższy poziom troficzny. Morskie ryby i owoce
					morza, takie jak skorupiaki, mięczaki, wodorosty są dla ludzi
					ważnym źródłem pożywienia. Spożywanie zanieczyszczonych organizmów
					morskich niesie za sobą negatywne skutki. Przykładem szkodliwej
					substancji jest metylortęć, która spożyta przez ciężarną kobietę
					negatywnie wpływa na rozwój mózgu i układu nerwowego płodu.
					Zanieczyszczenia wód morskich, dla których zostały ustanowione
					poziomy regulacyjne to m.in. pestycydy chloroorganiczne, związki
					cynoorganiczne, ftalany, bromowane opóźniacze płomienia,
					polifluorowane substancje chemiczne, wielopierścieniowe węglowodory
					aromatyczne, metale ciężkie, dioksyny. Substancje te mogą pochodzić
					z przypadkowych rozlań z platform morskich, hodowli morskich,
					ścieków ze statków a także z rolnictwa. Bioakumulacja to zdolność
					organizmów do kumulowania trujących związków w organizmie w
					stężeniu wyższym niż w otaczającym środowisku. A zatem im wolniej
					rozkłada się substancja toksyczna, tym większe jest ryzyko
					przewlekłego zatrucia, nawet jeżeli stężenie toksyn w środowisku
					nie jest bardzo wysokie.</p>
			</div>
			<div id="10">
				<h2>Odpady w środowisku morskim</h2>
				<p>Odpady morskie to problem globalny dotyczący wszystkich mórz i
					oceanów na świecie. Każdego roku miliony ton śmieci lądują w tych
					zbiornikach zmieniając je w największe wysypiska świata i powodując
					problemy środowiskowe, ekonomiczne, zdrowotne i estetyczne. Złe
					praktyki składowania odpadów stałych, brak infrastruktury i brak
					świadomości odnośnie konsekwencji podejmowanych działań stopniowo
					pogarszają sytuację. Sprzątanie akwenów nie jest najbardziej
					skuteczną metodą oczyszczania wód ze śmieci. Najlepszym
					rozwiązaniem jest zapobieganie marnowaniu oraz wdrażanie recyklingu
					i ponownego używania materiałów i produktów. Głównymi źródłami
					odpadów morskich są śmieci lądowe, rzeki i wody powodziowe, ujścia
					przemysłowe, ścieki komunalne, śmiecenie na plażach i rejonach
					przybrzeżnych, rybołówstwo, żegluga, nielegalne wysypiska w
					morzach. Morskie odpady mają także wpływ na ekonomię: straty dla
					społeczności nadmorskich, turystyki, żeglugi i rybołówstwa.</p>
			</div>
			<div id="11">
				<h2>Hałas podwodny</h2>
				<p>Źródłami hałasu podwodnego są wszystkie urządzenia mechaniczne
					pracujące w morzu takie jak statki, echosondy, pogłębiarki, koparki
					piachu, kable powodujące promieniowanie elektromagnetyczne, systemy
					chłodzenia wody, działania wojskowe. Dźwięki w wodzie mogą
					rozchodzić się w niej na znacznie większe odległości niż w
					powietrzu. Efekty wpływu hałasu wodnego na morskie zwierzęta nie
					zostały ostatecznie poznane. Obecnie trwają badania mające na celu
					ustalenie poziomów hałasu morskiego i jego wpływu na organizmy
					zamieszkujące morza.</p>
