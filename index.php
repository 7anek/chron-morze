				<h2>Integralność dna morskiego</h2>
				<p>To charakterystyka fizyczna, chemiczna i biologiczna dna
					morskiego. Czynniki te określają strukturę i funkcjonowanie
					morskich ekosystemów, szczególnie organizmów żyjących na dnie
					morskim. Specyfikę dna morskiego określają głębokość, typ podłoża
					(miękkie, twarde, biogenne) oraz zamieszkujące je gatunki.
					Działalność człowieka mająca wpływ na dno morskie to m.in.
					infrastruktura nabrzeżna i instalacje morskie (platformy
					wietrnicze, farmy wiatrowe); morskie kopalnie i wydobywanie piachu,
					uwalnianie wydobytego osadu, cumowanie, niektóre praktyki
					rybołówcze (np. trałowanie), hodowle wodne, wprowadzanie obcych
					gatunków, zanieczyszczanie, zmiany w składzie rzek
					(zanieczyszczenia organiczne), uwalnianie osadu przez sprzęt do
					rybołówstwa, uwalnianie dużej ilości ciepłej lub słonej wody.</p>
